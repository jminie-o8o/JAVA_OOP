
public class method {

		//public 리턴타입 메소드명 (매개변수 들) { 구현 }
		//여러가지 형태의 메소드
		public void method1() {
			System.out.println("m1이 실행됨");
		}
		
		public void method2(int x) {
			System.out.println(x +"을 이용한 m2 실행");
		}
		
		public int method3() {
			System.out.println("m3 실행");
			return 10;
		}
		
		public void method4(int x, int y) {
			System.out.println(x + y +"m4가 실행");
		}
		
		public int method5(int x) {
			System.out.println("m5가 실행");
			return x;
		}
}

